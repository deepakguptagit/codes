#define MAX 2000002

class NumbersChallenge {
public:

	int getSum(int i,vector<int> S){
		int sz = S.size();
		int sum = 0;
		int count = 0;
		while(count < sz && i != 0){
		    sum += S[count]*(i&1);
		    count++;
		    i = i >> 1;
		}
		return sum;
	}
	
	int MinNumber(vector <int> S) {
		int possibleSums[MAX] = {0};
		int N = 1 << (S.size());
		for(int i=0;i<N;i++){
		   int sum = getSum(i,S);
		   	possibleSums[sum]++;
		}
		for(int i=1;i<MAX;i++){
			if(possibleSums[i] == 0) return i;
		}	
	}
};


