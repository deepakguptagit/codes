class PotentialGeometricSequence {
public:

	int isAP(vector<int> v,int i,int j){
		if(v.size() == 0 || v.size() == 1 || v.size() == 2 || (j-i+1)<=2) return 1;
		int d = v[i+1] - v[i];
		for(int k=i+2;k<=j;k++){
			if(v[k] != v[k-1]+d) return 0;
		}
		return 1;
	}
	
	int numberOfSubsequences(vector <int> d) {
	   int sz = d.size();
	   int ans = 0;
	   for(int i=0;i<d.size();i++){
	       for(int j=i+1;j<d.size();j++){
	       	   ans += isAP(d,i,j);
	       }
	   }
	   return ans + d.size();
	}
};


